var _currentPageContent = "";
function transformText(content, probability, deletedEntries) {
  if (!content) return "";
  var deletedMap = {};
  if (deletedEntries) {
    if (Array.isArray(deletedEntries)) {
      for (var d = 0; d < deletedEntries.length; d++) {
        deletedMap[deletedEntries[d]] = true;
      }
    } else {
      for (var key in deletedEntries) {
        if (deletedEntries[key]) deletedMap[key] = true;
      }
    }
  }
  var lines = String(content).split("\n");
  for (var l = 0; l < lines.length; l++) {
    var line = lines[l];
    if (!line.trim()) continue;
    var matches = [];
    for (var w = 0; w < SORTED_VOCAB.length; w++) {
      var item = SORTED_VOCAB[w];
      if (!item || !item[1]) continue;
      var cn = item[0];
      var en = item[1];
      var key = cn + "|" + en;
      if (deletedMap[key]) continue;
      var idx = 0;
      while ((idx = line.indexOf(cn, idx)) >= 0) {
        matches.push({
          index: idx,
          length: cn.length,
          chinese: cn,
          english: en,
        });
        idx += cn.length;
      }
    }
    if (matches.length === 0) continue;
    matches.sort(function (a, b) {
      return a.index - b.index;
    });
    var filtered = [];
    var lastEnd = -1;
    for (var m = 0; m < matches.length; m++) {
      if (matches[m].index >= lastEnd) {
        filtered.push(matches[m]);
        lastEnd = matches[m].index + matches[m].length;
      }
    }
    filtered.sort(function (a, b) {
      return b.index - a.index;
    });
    for (var m = 0; m < filtered.length; m++) {
      if (Math.random() >= probability) continue;
      var match = filtered[m];
      var rep = match.english + "（" + match.chinese + "）";
      line =
        line.substring(0, match.index) +
        rep +
        line.substring(match.index + match.length);
    }
    lines[l] = line;
  }
  return lines.join("\n");
}
var DEFAULT_PROBABILITY = 0.1;
async function _pickCandidate(candidates, eng, api) {
  var items = [];
  for (var c = 0; c < candidates.length; c++) {
    items.push(candidates[c].cn + " → " + eng);
  }
  try {
    if (api.ui && typeof api.ui.select === "function") {
      var choice = await api.ui.select("选择要删除的键值对", items);
      if (typeof choice === "number" && choice >= 0 && choice < items.length) {
        return candidates[choice].cn;
      }
    }
  } catch (e1) {}
  for (var c = 0; c < candidates.length; c++) {
    try {
      if (api.ui && typeof api.ui.confirm === "function") {
        var ok = await api.ui.confirm(items[c]);
        if (ok) return candidates[c].cn;
        continue;
      }
    } catch (e2) {}
    if (confirm(items[c])) return candidates[c].cn;
  }
  return null;
}
